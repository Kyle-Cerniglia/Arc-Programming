
public class javaVariables {
	
	public static void main(String []args){
		
		int first_number, second_number, answer; // Var's can't start with a number and NO SPACES!
		 // Also notice the stacked int var's
		
		first_number = 10;
		second_number = 20;
		answer = first_number + second_number; // Operations go AFTER the answer
		
		System.out.println("Addition total " + answer);
		
		
	}

}

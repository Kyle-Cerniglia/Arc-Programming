
public class stringVariables {
	
	public static void main(String []args){
		
		String first_name; // Uppercase 'S', defines a string var
		String family_name;
		char mid; // Stores a single character var
		
		first_name = "Kyle";
		family_name = "Cernigla";
		mid = 'N'; // Char var's have '' while string's have ""
		
		System.out.println(first_name + " " + mid + " " + family_name); // " " for space between the names
		
	}

}
